public class App {
    public static void main(String[] args) throws Exception {
        
        int x, y;
        x = 5;
        y = 2 * x;

        System.out.println(x);
        System.out.println(y);

        double d;
        d = 2.0;
        y = 2 * (int) d; // casting
        System.out.println(d);
        System.out.println(y);


    }
}
